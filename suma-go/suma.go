package main

import "fmt"

func main() {
	var numero1 int = 5
	var numero2 int = 10

	fmt.Print("La suma es igual: ")
	fmt.Println(numero1 + numero2)

	fmt.Print("La resta es igual: ")
	fmt.Println(numero1 - numero2)
	fmt.Print("La multiplicacion es igual: ")
	fmt.Println(numero1 * numero2)
	fmt.Print("La divicion es igual: ")
	fmt.Println(numero1 / numero2)
	fmt.Print("El resto es igual: ")
	fmt.Println(numero1 % numero2)
}
